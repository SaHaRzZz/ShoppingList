import kivy
from kivy.app import App
from kivy.uix.label import Label
from kivy.uix.gridlayout import GridLayout
from kivy.uix.gridlayout import GridLayout
from kivy.uix.textinput import TextInput
from kivy.uix.button import Button
from kivy.uix.scrollview import ScrollView
from kivy.graphics import Color, Rectangle
from kivy.uix.screenmanager import ScreenManager, Screen
from kivy.core.clipboard import Clipboard

global toClipboard
toClipboard = ""

class MyScreen(ScreenManager):
    def __init__(self, **kwargs):
        super(MyScreen, self).__init__(**kwargs)

        self.mainFirstGrid = GridLayout(cols=1, rows=2)
        self.mainSecondGrid = GridLayout(cols=1, rows=2)

        self.submitButton = Button(text=rtl("הכנת רשימה"), font_size=80, font_name="Fonts/David", size_hint_y=0.1, background_color=[0.32, 0.2, 0.93, 1])
        self.backButton = Button(text=rtl("אחורה"), font_size=80, font_name="Fonts/David", size_hint_y=0.1)
        self.copyButton = Button(text=rtl("העתקת רשימה"), font_name="Fonts/David", size_hint_y=None, height=150, font_size=60, background_color=[0.32, 0.2, 0.93, 1])
        self.copyButton.text = rtl("העתקת רשימה")
        
        self.submitButton.bind(on_press=self.submitPressed)
        self.backButton.bind(on_press=self.backPressed)
        self.copyButton.bind(on_press=self.copyClipboard)
        self.firstScreen = Screen(name="1")
        self.secondScreen = Screen(name="2")
        self.add_widget(self.firstScreen)
        self.add_widget(self.secondScreen)
        self.firstScreen.add_widget(self.mainFirstGrid)
        self.secondScreen.add_widget(self.mainSecondGrid)

        self.firstInside = GridLayout(cols=1, rows=listLength+subLength+mainLength, size_hint_y=None)
        self.firstInside.bind(minimum_height=self.firstInside.setter('height'))
        self.secondInside = GridLayout(size_hint_y=None)
        self.secondInside.bind(minimum_height=self.secondInside.setter('height'))
        self.firstScrollInside = ScrollView()
        self.firstScrollInside.add_widget(self.firstInside)
        self.secondScrollInside = ScrollView()
        self.secondScrollInside.add_widget(self.secondInside)
        self.mainFirstGrid.add_widget(self.firstScrollInside)
        self.mainFirstGrid.add_widget(self.submitButton)
        self.mainSecondGrid.add_widget(self.secondScrollInside)
        self.mainSecondGrid.add_widget(self.backButton)

        for main in mainList:
            self.firstInside.add_widget(Label(text=":" + main, font_name="Fonts/aharoniclm-bold-webfont", underline=True, size_hint_y=None, font_size=90, height=105, color=[0.95, 0.15, 0.07, 1]))
            for sub in mainList[main]:
                self.firstInside.add_widget(Label(text=":" + sub, font_name="Fonts/aharoniclm-book-webfont", size_hint_y=None, font_size=75, height=150, color=[0.32, 0.2, 0.93, 1]))
                for itemNum in mainList[main][sub]:
                    tempLabel = Label(text='[ref=' + mainList[main][sub][itemNum]["item"] + ']' + mainList[main][sub][itemNum]["item"] + '[/ref]', markup=True, font_name="Fonts/VarelaRound-Regular", size_hint_y=None, height=75, font_size="60", halign="right", base_direction="rtl")

                    tempLabel.bind(on_ref_press=self.itemFunction)
                    mainList[main][sub][itemNum]["label"] = tempLabel
                    tempLabel.selected = False
                    self.firstInside.add_widget(tempLabel)

    def itemFunction(self, instance, value):
        if instance.selected:
            instance.color = [1, 1, 1, 1]
            instance.selected = False
        else:
            instance.color = [0, 0.90, 0.25, 1]
            instance.selected = True
    
    def copyClipboard(self, instance):
        Clipboard.copy(toClipboard)
        if toClipboard == "":
            Clipboard.copy("\n")
        self.copyButton.text = rtl("רשימה הועתקה בהצלחה!")
        self.copyButton.background_color=[0, 0.90, 0.25, 1]
    
    def updateSecond(self):
        finalList = []
        for main in mainList:
            for sub in mainList[main]:
                for itemNum in mainList[main][sub]:
                    if mainList[main][sub][itemNum]["label"].selected:
                        finalList.append(mainList[main][sub][itemNum]["item"])

        self.secondInside.clear_widgets()
        self.copyButton.text = rtl("העתקת רשימה")
        self.copyButton.background_color = [0.32, 0.2, 0.93, 1]
        self.secondInside.add_widget(self.copyButton)
        self.secondInside.cols = 1
        self.secondInside.rows = len(finalList) + 1

        mainDict.resetFileList()
        
        global toClipboard
        toClipboard = ""
        for item in finalList:
            self.secondInside.add_widget(Label(text=item, font_name="Fonts/aharoniclm-bookoblique-webfont", size_hint_y=None, height=75, font_size=60))
            toClipboard += rtl(item) + "\n"
            mainDict.addItemToFileList(item)

    def submitPressed(self, instance):
        self.submitButton.background_color = [0, 0.90, 0.25, 1]

        self.backButton.background_color=[0.32, 0.2, 0.93, 1]

        self.updateSecond()
        self.current = "2"
        
    def backPressed(self, instance):
        self.backButton.background_color = [0, 0.90, 0.25, 1]

        self.submitButton.background_color=[0.32, 0.2, 0.93, 1]

        self.current = "1"

class ShoppingListApp(App):
    def build(self):
        return MyScreen()

ShoppingListApp().run()
